// TIPOS DE DATOS

/*  JavaScript cuenta con 7 tipos de datos primitivos: */

// Booleano (true o false)
a = true;

// Null (palabra clave especial que denota un valor nulo)
b = null;

// Undefined (propiedad de alto nivel cuyo vaor no esta definido)
c = undefined;

// Number (un numero entero o un numero con coma flotante)
d = 123;
e = 123.456;

// BigInt (un numero entero con precision arbitraria)
f = 9007199254740992n;

// String (una secuencia de caracteres)
g = "Hola Mundo";

// Symbol (tipo de dato cuyas instancias son unicas e inmutables)

h = Symbol("Hola Mundo");

// Object (un objeto), piensa que son como contenedores con nombre para los valores, y las funciones como procedimientos que puedes programar en tu aplicacion
i = {
    propiedad: "valor"
};