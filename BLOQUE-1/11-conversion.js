// CONVERSIONES DE TIPOS DE DATOS

// JavasScript es un lenguaje de tipado dinamicamente. Significa que no tienes que especificar el tipo de dato de una variable cuando la declaras. Tambien significa que los tipos de datos se convierten automaticamente segun sea necesario durante la ejecucion del script. Por ejemplo:

var answer = 42;
answer = "Olaaa";
console.log(answer); // Olaaa