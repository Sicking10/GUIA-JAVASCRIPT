// DECLARACIONES

// JavaScript tiene tres tipos de declaraciones de variables:

// var: declara una variable, opcionalmente la inicia con un valor

var ejemplo = "hola";
var ejemplo;

// let: declara una variable local con ambito de bloque, opcionalmente la inicia con un valor

let ejemplo1 = "hola";
let ejemplo2;

// const: declara un nombre de constante de solo lectura y ambito de bloque, siempre la inicia con un valor

const ejemplo3 = "hola";

