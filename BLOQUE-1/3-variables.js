// DECLARACIONES DE VARIABLES

// Pudes declarar una variable de dos formas:

// Con la palabra clave var. Por ejemplo:

var xr = 42;

// Esta sintaxis se puede utilizar para declarar variables locales y globales, dependiendo del contexto de ejecucion

// Con la palabra clave const o let. Por ejemplo:

let yr = 13;
const zr = 3;

// Esta sintaxis se puede utilizar para declarar una variable local con ambito de bloque

// Tambien se puede simplemente asignar un valor. Por ejemplo:

xrr = 42;

// Esto crea una variable global no declarada y genera una advertencia estricta de JavaScript, por lo que no es muy recomendable usar esta sintaxis.